<script>
  $(document).ready(function() {
    var max_fields = 10; //maximum input boxes allowed
    var wrapper    = $(".input_fields_wrap"); //Fields wrapper
    var add_button = $("#button-plus"); //Add button ID
    var html       = '';
      html += '<div class="d-flex mb-3">';
      html += '<div class="input-group">';
      html += '<span class="input-group-text" id="basic-addon3">Anak 2</span>';
      html += '<input type="text" class="form-control" name="pekerjaan-anak-[]" placeholder="Pekerjaan Anak []">';
      html += '</div>';
      html += '<button type="button" class="btn btn-danger ms-3 remove_field" id="input1" style="padding: 0;width: 42px;"><i class="fa-solid fa-minus"></i></button>';
      html += '</div>';
    
    var x = 1; //initlal text box count
    $(add_button).click(function(e){ //on add input button click
      e.preventDefault();
      if(x < max_fields){ //max input box allowed
        x++; //text box increment
        $(wrapper).append(html); //add input box
      }
    });
    
    $(wrapper).on("click",".remove_field", function(e){ //user click on remove text
      e.preventDefault(); $(this).parent('div').remove(); x--;
    })
  });
</script>